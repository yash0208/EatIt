package com.rajaryan.eatit;

public class steps {
    String number,step;

    public steps() {
    }

    public steps(String number, String step) {
        this.number = number;
        this.step = step;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getStep() {
        return step;
    }

    public void setStep(String step) {
        this.step = step;
    }
}

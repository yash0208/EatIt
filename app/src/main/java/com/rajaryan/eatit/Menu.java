package com.rajaryan.eatit;

public class Menu {
    String name,hotel,img,id;

    public Menu() {
    }
    public Menu(String name, String hotel, String img, String id) {
        this.name = name;
        this.hotel = hotel;
        this.img = img;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getHotel() {
        return hotel;
    }

    public void setHotel(String hotel) {
        this.hotel = hotel;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
